// ================================
// 변수 선언 & 할당
// ================================

var score;       // 변수 선언
score = 80;      // 값의 할당
var score = 80;  // 선언 + 할당


// ================================
// 변수의 역할
// ================================

// 변수는 하나의 값을 저장하기 위한 수단
var userId = 1;
var userName = 'Lee';

// 객체/배열로 여러 값을 하나처럼 사용
var user = { id: 1, name: 'Lee' };

var users = [
  { id: 1, name: 'Lee' },
  { id: 2, name: 'Kim' }
];


// ================================
// 변수 선언 vs 값 할당 시점
// ================================

// 선언 → 런타임 이전
// 할당 → 런타임

console.log(score); // undefined
var score;          // 변수 선언
score = 80;         // 값 할당
console.log(score); // 80


// ================================
// 재할당
// ================================

// 이미 값이 있는 변수에 새로운 값을 다시 넣는 것
var score = 80;
score = 90; // 재할당

