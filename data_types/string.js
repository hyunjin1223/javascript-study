// ==============================
// 문자열(String) 타입
// ==============================

// 문자열은 0개 이상의 문자 집합
// → UTF-16 기반으로 대부분의 문자 표현 가능

// ==============================
// 문자열 선언 방법
// ==============================

var string;

string = '문자열';   // 작은따옴표 (가장 일반적)
string = "문자열";   // 큰따옴표
string = `문자열`;   // 백틱 (ES6)

// ==============================
// 따옴표 사용 규칙
// ==============================

// 서로 다른 따옴표를 내부에서 사용 가능
string = '작은따옴표 안에 "큰따옴표" 사용 가능';
string = "큰따옴표 안에 '작은따옴표' 사용 가능";

// ==============================
// 주의사항
// ==============================

// 따옴표 없이 작성하면 문자열이 아니라 "식별자"로 인식됨
var string = hello;  // ReferenceError (hello는 변수로 인식됨)